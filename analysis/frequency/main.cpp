#include <iostream>
#include <cmath>
#include <list>
#include <fftw3.h>
#include "sine.h"
#include "mixer.h"
#include "fir.h"

using namespace std;

#define FFT_SIZE (8192)

#define SAMP_FREQ (250.0)
#define ADC_SAMPLES (1024 + FFT_SIZE * 10)
#define OVERSAMPLING (20)
#define SAMPLES (ADC_SAMPLES * OVERSAMPLING)

#define FREQ_LO1 (1413.0 - 374.0)
#define FREQ_LO2 (374.0 + 62.5)

extern double fir_5GSps_374MHz[993];
extern double fir_5GSps_100MHz_LP[512];
extern double fir_decimation[160];

void createSignalChain(std::list<module *> &signalChain, double freqRf, double freqSamp);
void toPolar(double x, double y, double &mag, double &angle);

int main(int argc, char **argv) {
    std::list<module *> signalChain;
    createSignalChain(signalChain, 1403.0, SAMP_FREQ * OVERSAMPLING);

    double output[ADC_SAMPLES];
    for(unsigned i = 0; i < SAMPLES; i++) {
        for(auto m : signalChain)
            m->doStep();

        if(((i+1) % OVERSAMPLING) == 0) {
            output[i / OVERSAMPLING] = signalChain.back()->getOutput().getValue() * 4;
        }
    }

    // multiply IQ samples by Nyquist frequency
    for(unsigned i = 0; i < ADC_SAMPLES; i++) {
        if((i % 4) >= 2)
            output[i] = -output[i];
    }

    // FIR decimation filter
    for(unsigned i = ADC_SAMPLES - 1; i >= 320; i--) {
        double acc = 0;
        for(unsigned j = 0; j < 160; j++) {
            acc += output[i - 2 * j] * fir_decimation[j];
        }
        output[i] = acc;
    }

    fftw_complex in[FFT_SIZE], out[FFT_SIZE];
    auto p = fftw_plan_dft_1d(FFT_SIZE, in, out, FFTW_FORWARD, FFTW_ESTIMATE);

    // split IQ samples
    for(unsigned i = 0; i < FFT_SIZE * 10; i += 10) {
        in[i / 10][0] = output[1024 + i + 0];
        in[i / 10][1] = output[1024 + i + 1];
    }

    fftw_execute(p);
    fftw_destroy_plan(p);

    std::list<std::pair<double, unsigned> > bins;
    double mag, angle;
    for(unsigned i = 0; i < FFT_SIZE; i++) {
        toPolar(out[i][0], out[i][1], mag, angle);
        bins.emplace_back(std::make_pair(mag, i));
    }
    bins.sort();

    auto it = bins.rbegin();
    for(int i = 0; i < 20; i++) {
        if(it->second <= FFT_SIZE / 2) {
            cout << 0.1*(SAMP_FREQ * it->second / FFT_SIZE) << ": " << 20*log10(it->first) << endl;
        } else {
            cout << 0.1*(-SAMP_FREQ * (FFT_SIZE - it->second) / FFT_SIZE) << ": " << 20*log10(it->first) << endl;
        }
        it++;
    }

    return 0;
}

void createSignalChain(std::list<module *> &signalChain, double freqRf, double freqSamp) {
    auto rf = new sine(2 * M_PI * freqRf / freqSamp, 0);
    signalChain.push_back(rf);

    auto lo1 = new sine(2 * M_PI * FREQ_LO1 / freqSamp, 0);
    signalChain.push_back(lo1);

    auto lo2 = new sine(2 * M_PI * FREQ_LO2 / freqSamp, 0);
    signalChain.push_back(lo2);

    auto mix1 = new mixer(rf->getOutput(), lo1->getOutput());
    signalChain.push_back(mix1);

    auto filtIf1 = new fir(mix1->getOutput(), fir_5GSps_374MHz, 993);
    signalChain.push_back(filtIf1);

    auto mix2 = new mixer(filtIf1->getOutput(), lo2->getOutput());
    signalChain.push_back(mix2);

    auto filtIf2 = new fir(mix2->getOutput(), fir_5GSps_100MHz_LP, 512);
    signalChain.push_back(filtIf2);
}

void toPolar(double x, double y, double &mag, double &angle) {
    mag = hypot(x, y);
    angle = atan2(y, x);
}

